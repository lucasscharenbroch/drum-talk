<h2 id="language">The Language</h2>
<h3>Counting (Absolute Words)</h3>
<ul>
    <li>1, 2, 3, 4, 5, 6, ...</li>
    <li>one, two, three, four, five, six, ...</li>
    <li>e, and, &, +, a, ah</li>
</ul>
<h3>Strokes (Relative Words)</h3>
<ul>
    <li>tap, t, ., !</li>
    <li>gock, shot, x</li>
    <li>buzz, z</li>
    <li>flam, f, '</li>
    <li>drag, dr, "</li>
    <li>double, d, -</li>
    <li>roll, =</li>
</ul>
<h3>Rudiments (Relative Words)</h3>
<ul>
    <li>flamtap, ft</li>
    <li>flamaccent, fac</li>
    <li>flamacue, fac</li>
    <li>pataflafla, ptff</li>
    <li>twentyfive, ttf</li>
    <li>ratamacue, rtmc</li>
    <li>paradiddle, padd</li>
    <li>paraparadiddle, papadd</li>
    <li>paraparaparadiddle, papapadd</li>
    <li>paradiddlediddle, padddd</li>
    <li>dragadiddle, dradd</li>
    <li>tripulet, tpl</li>
    <li>swipulet, sat</li>
</ul>
<h3>Misc. Sounds (Relative Words)</h3>
<ul>
    <li>da, ta: long tap</li>
    <li>duh, tuh: short tap</li>
    <li>dduh, ttuh: double-stroke</li>
    <li>ddut, ttut: flam</li>
</ul>
<h3>Groups</h3>
<ul><li>Surround any non-absolute words in parenthesis to form a group: groups can be used to form tuplets and/or establish relative sticking</li></ul>
<h3>Accenting</h3>
<ul><li>Capitalize any syllable of an alphabetic word to add an accent to it</li></ul>
<h3>Modifiers</h3>
<p>The following flags can be placed in braces (curly brackets) before a (non-rudiment) word to modify it accordingly</p>
<ul>
    <li>z: buzz</li>
    <li>-: double-stroke</li>
    <li>=: long-roll</li>
    <li>x: gock/shot/click/x-note</li>
    <li>&gt;: accent</li>
    <li>^: marcato</li>
    <li>': flam</li>
    <li>f: flam</li>
    <li>": drag</li>
    <li>n: neutral stick (always alternate from previous)</li>
    <li>l: weak left stick (ignored, except in groups)</li>
    <li>r: weak right stick (ignored, except in groups)</li>
    <li>L: strong left stick</li>
    <li>R: strong right stick</li>
</ul>
<h3>Duration Modification</h3>
<p>Place the following before any word or group to modify its duration</p>
<ul>
    <li>&lt;1&gt;: whole</li>
    <li>&lt;2&gt;: half</li>
    <li>&lt;4&gt;: quarter</li>
    <li>&lt;8&gt;: eighth</li>
    <li>&lt;16&gt;: sixteenth</li>
    <li>&lt;32&gt;: thirty-second</li>
</ul>

